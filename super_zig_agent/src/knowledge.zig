/// البحث في قاعدة المعرفة
pub fn search(query: []const u8) ?[]const u8 {
    var best_match: ?[]const u8 = null;
    var best_score: usize = 0;

    for (KNOWLEDGE_BASE) |entry| {
        for (entry.keywords) |keyword| {
            if (std.mem.indexOf(u8, query, keyword) != null) {
                // كلما طالت الكلمة المفتاحية، كان المطابقة أفضل
                if (keyword.len > best_score) {
                    best_score = keyword.len;
                    best_match = entry.response;
                }
            }
        }
    }

    return best_match;
}
